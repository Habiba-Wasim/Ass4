# File: add2numbers.py

def main():
    print("This program adds two numbers.")
    
    # User se inputs lena
    num1 = int(input("Enter the first number: "))
    num2 = int(input("Enter the second number: "))
    
    # Sum calculate karna
    total = num1 + num2
    
    # Result print karna
    print("The total is " + str(total) + ".")


# Program yahan se start hota hai
if __name__ == '__main__':
    main()
